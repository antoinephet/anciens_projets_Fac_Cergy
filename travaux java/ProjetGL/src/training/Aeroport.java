package training;

public class Aeroport {
	
	private String nom;
	private String id;
	
	
	public Aeroport(String nom, String id) {
		
		this.nom = nom;
		this.id = id;
	}


	public String getNom() {
		return nom;
	}


	public void setNom(String nom) {
		this.nom = nom;
	}


	public String getId() {
		return id;
	}


	public void setId(String id) {
		this.id = id;
	}


	@Override
	public String toString() {
		return "Aeroport [nom=" + nom + ", id=" + id + "]";
	}
	
	
	
	
	
	

}
